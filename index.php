        function share() {
            let url = document.location.href;
            const canonicalElement = document.querySelector('link[rel=canonical]');
            if (canonicalElement !== null) {
                url = canonicalElement.href;
            }

            const shareOptions = {
                title: 'Compartilhar este link',
                text: 'Confira este link interessante:',
                url: url,
            };

            if (navigator.share) {
                // Se o navegador suportar a API Web Share, use-a
                navigator.share(shareOptions)
                    .then(() => {
                        console.log('URL compartilhado com sucesso.');
                    })
                    .catch(error => {
                        console.error('Erro ao compartilhar o URL: ', error);
                    });
            }
        }
    </script>
